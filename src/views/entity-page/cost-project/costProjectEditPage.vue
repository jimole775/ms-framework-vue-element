<template>
  <div class="container">
    <CostProjectEdit ref="costProjectEdit" :id="id" @loadingChange="loadingChange" />    
    <div class="max-w">
      <div class="f-r">
        <el-button v-loading="loading" size="mini" @click="resetData">重置</el-button>
        <el-button v-loading="loading" type="primary" size="mini" @click="save">保存</el-button>
      </div>
    </div>
  </div>
</template>

<script>
import CostProjectEdit from './../../../components/entity/cost-project/CostProjectEdit'
export default {
  name: 'CostProjectEditPage',
  components: {
    CostProjectEdit
  },
  data() {
    return {
      loading: false,
      id: null
    }
  },
  created() {
    this.id = this.$route.query.id
  },
  methods: {
    save() {
      this.$refs.costProjectEdit.save()
    },
    resetData() {
      this.$refs.costProjectEdit.resetData()
    },
    loadingChange(val) {
      this.loading = val
    }
  }
}
</script>

