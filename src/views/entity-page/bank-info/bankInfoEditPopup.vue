<template>
  <div>
    <el-dialog
      :visible="true"
      :width="windowWidth < 768 ? 'calc(100% - 24px)' : '80%'"
      :title="id ? '银行信息-edit' : 'new-银行信息'"
      top="10vh"
      @close="close"
    >
      <div>
        <BankInfoEdit ref="bankInfoEdit" :id="id" @loadingChange="loadingChange" />
      </div>
      <div slot="footer" class="dialog-footer">
        <el-button v-loading="loading" size="mini" @click="resetData">重置</el-button>
        <el-button v-loading="loading" type="primary" size="mini" @click="save">保存</el-button>
      </div>
    </el-dialog>
  </div>
</template>

<script>
import BankInfoEdit from './../../../components/entity/bank-info/BankInfoEdit'
export default {
  name: 'BankInfoEditPopup',
  components: {
    BankInfoEdit
  },
  props: {
    id: {
      type: String | Number,
      default: null
    }
  },
  data() {
    return {
      loading: false
    }
  },
  computed: {
    windowWidth() {
      return this.$store.getters.width
    }
  },
  created() {
  },
  methods: {
    close() {
      this.$emit('close')
    },
    async save() {
      const formData = await this.$refs.bankInfoEdit.save()
      this.$emit('update', formData)
    },
    resetData() {
      this.$refs.bankInfoEdit.resetData()
    },
    loadingChange(val) {
      this.loading = val
    }
  }
}
</script>

