<template>
  <div class="container">
    <FeePayEdit ref="feePayEdit" :id="id" @loadingChange="loadingChange" />    
    <div class="max-w">
      <div class="f-r">
        <el-button v-loading="loading" size="mini" @click="resetData">重置</el-button>
        <el-button v-loading="loading" type="primary" size="mini" @click="save">保存</el-button>
      </div>
    </div>
  </div>
</template>

<script>
import FeePayEdit from './../../../components/entity/fee-pay/FeePayEdit'
export default {
  name: 'FeePayEditPage',
  components: {
    FeePayEdit
  },
  data() {
    return {
      loading: false,
      id: null
    }
  },
  created() {
    const id = this.$route.params.id
    this.id = id != null && id !== 'new' ? id : null
  },
  methods: {
    save() {
      this.$refs.feePayEdit.save()
    },
    resetData() {
      this.$refs.feePayEdit.resetData()
    },
    loadingChange(val) {
      this.loading = val
    }
  }
}
</script>

